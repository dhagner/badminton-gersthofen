<h3>Textfelder (XHTML erlaubt)</h3>
<p>Einige, speziell markierte Textfelder erlauben die Eingabe von XHTML-konformen Textauszeichnungen. Im Gegensatz zu
gewöhnlichen Textfeldern findet hier keine Konversion von Spezialzeichen statt, da ansonsten die Bedeutung der
Austeichnungs-Tags verloren gehen würde. Nachfolgend sind einige Beispiele notiert, die die korrekte Verwendung einiger
XHTML-Tags zeigen:</p>

<p>Ein Textteil kann betont (fett dargestellt) werden, indem er in den XHTML-Tags &lt;em&gt; und &lt;/em&gt;
eingeschlossen wird. Das letzte Wort dieses Satzes kann z. B. wie gezeigt <em>&lt;em&gt;betont&lt;/em&gt;</em> werden.</p>

<p>Auf eine Seite im WWW kann mittels der &lt;a&gt;&lt;/a&gt;-Tags verlinkt (unterstrichen dargestellt) werden. Der
folgende Link verweist z. B. auf unseren <em>&lt;a href="http://www.tsv-gersthofen.de"&gt;Hauptverein&lt;/a&gt;</em>.
Für Website-Interne Links besteht übrigens die komfortable Möglichkeit, den 'section='-String in geschweifte Klammern zu
setzen. Mit 'section='-String ist dasjenige Wort gemeint, welches in der Adressleiste Deines Browsers ganz rechts hinter
'section=' ablesbar ist, wenn Du die Zielseite aufgerufen hast. Der lesbare Text, auf ein Besucher klicken kann und
hinter dem sich dann der Link verbirgt, ist übrigens der gleiche wie der Text, den man auch in der Navigationsleiste
lesen kann. Beispielsweise führt das Konstrukt '<em>{training}</em>' (Kleinschreibung beachten!) zu einem Link auf die
Seite 'Training'. Einfach mal ausprobieren, schließlich kann man die Texte hier jederzeit problemlos korrigieren.</p>

<p>Übrigens: Der XHTML-Tag für den Zeilenumbruch '&lt;br /&gt;' braucht nicht explizit eingefügt zu werden. Neue Zeilen
werden automatisch erkannt und entsprechend formatiert.</p>


