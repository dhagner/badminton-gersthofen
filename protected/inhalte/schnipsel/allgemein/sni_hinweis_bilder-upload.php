<h3>Upload von Bilder</h3>
<p><q>Bilder sagen mehr als tausend Worte.</q> Dieser Spruch gilt insbesondere dann, wenn das Bild auch mit seiner
Umgebung harmoniert. Damit sich hochgeladene Bilder stimmig in das Layout der Seiten integrieren, werden diese
grundsätzlich mit einer festen Höhe dargestellt und sind in ihrem Seitenverhältnis (Breite:Höhe) beschränkt. Dein Foto
präsentiert sich am wirkungsvollsten, wenn Du auf folgende Seitenverhältnisse achtest:</p>
<ul>
	<li>3:4 für Portraitfotos</li>
	<li>4:3 für Landschaftfotos</li>
	<li>4:3 oder 3:4 für Sportszenen</li>
	<li>16:9 für einen Kino-Effekt</li>
	<li>16:9 für abfotografierte Schriftzüge</li>
</ul>
<p>Grundsätzlich gelten außerdem die folgenden Einschränkungen:</p>
<ul>
	<li>Die Bilddatei muss vom 'JPEG'-Format sein.</li>
	<li>Die maxiaml erlaubte Dateigröße beträgt <?php echo round(MAX_FILE_SIZE_ATTACH_PIC/(1024*1024), 2).'&#x202F;MB ' ?></li>
	<li>Das maximal zulässige Seitenverhältnis beträgt <?php echo MAX_IMG_RATIO_BIGPART.':'.MAX_IMG_RATIO_SMLPART ?>
	(Breite:Höhe)</li>
	<li>Das minimal zulässige Seitenverhältnis beträgt <?php echo MAX_IMG_RATIO_SMLPART.':'.MAX_IMG_RATIO_BIGPART ?>
	(Breite:Höhe)</li>
</ul>
<p>Ist die Breite oder Höhe Deines Bildes größer als <?php echo MAX_IMG_DIM ?> Pixel, so wird Dein Bild übrigens
automatisch skaliert.</p>
<p>Wenn Du ein bestehendes Bild mit einem neuen Bild ersetzt, siehst Du nach der "Speichern"-Aktion möglicherweise immer
noch das alte Bild. Dies liegt daran, dass Dein Internet-Browser (z. B. Internet Explorer oder Firefox) große Datein,
wie z. B. Bilddateien, in ein temporäres Verzeichnis auf Deiner Festplatte zwischenspeichert, um diese nicht ständig neu
aus dem Internet herunterladen zu müssen. Du musst in diesem Fall einmal auf "Aktualisieren" klicken, damit der Borwser
dazu gezungen wird, die komplette Seite neu herunterzuladen.</p>
