<h1>Klassen-Debugging</h1>

<p>Dieser Bereich wurde für die PHP-Entwicklung eingerichtet. Neue PHP-Klassen können hier mittels eines
standardisierten Ablaufs getestet werden. Es bietet sich an, das Debug-Modul (beginnt mit 'mod_dbg_') einer bestehenden
Klasse zu kopieren, umzubenennen und entsprechend den Erfordernissen der neuen Klasse anzupassen.</p>
