<h1>Training</h1>

<p>Du interessierst Dich für unsere Sparte und möchtest mehr über uns und unsere Trainingszeiten erfahren? Die Sparte
bietet Übungsstunden für folgende Gruppen an:</p>

<ul>
	<li>Schüler (8-14 Jahre)</li>
	<li>Jugend (15-18 Jahre)</li>
	<li>Aktive (ab 18 Jahre)</li>
	<li>Hobbyspieler</li>
</ul>

<p>Dabei spielt es keine Rolle, ob Du Anfänger, erfahrener Freizeitspieler oder Profi bist. Alle Leistungs- und
Altersgruppen sind bei uns herzlich willkommen. Weitere Informationen zum Training findest in den Unterrubriken bzw.
erhältst Du direkt vom <a href="index.php?section=kontakt">Cheftrainer</a>.</p>

