<h2>Wichtiger Hinweis zur Browser-Anforderung</h2>
<p>Für die hier angebotenen Funktionalitäten wird ein standardkonformer XHTML-Browser benötigt. Beschränkt man sich auf
die beiden am meisten verbreiteten Browser "Firefox" und "Internet Explorer", so benötigt man beim Firefox mindestens
Verison 3 bzw. beim Internet Explorer mindestens Version 8.</p>
<p>Grund: <br />
http://de.selfhtml.org/html/formulare/formularbuttons.htm#modern<br />
<q>Der Internet Explorer [...] verhält sich bei dieser Art Button leider falsch. Er sendet die Buttonbeschriftung, nicht
den Inhalt des value-Attributes. Existieren mehrere Absende-Buttons, werden die Beschriftungen aller Buttons gesendet,
nicht nur die des geklickten. Deshalb muss man dem IE leider attestieren, dass er dieses Feature derzeit noch nicht
korrekt unterstützt.</q></p>

