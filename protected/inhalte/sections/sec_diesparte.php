<h1>Sparteninfos</h1>

<p>Badminton ist eine Sparte der <a href="http://www.tsv-gersthofen.de/Leichtathletik/index.htm"
<?php echo STD_NEW_WINDOW ?>>Abteilung Leichtathletik</a> und ging 1995 aus einer Gruppe von Hobbyspielern hervor. Der
Großteil der Gründungsmitglieder ist auch heute noch dabei. Nach einjährigem Training nahmen die Spielerinnen und
Spieler erstmals 1996 am Punktspielbetrieb teil. Zur gleichen Zeit kamen eine Schüler- und eine Jugendgruppe dazu.</p>

<p>Sportlich ging es stetig bergauf: 1998 Aufstieg in die B-Klasse, 2002 in die A-Klasse, 2007 in die Bezirksoberliga.
Fortwährend sind mindesten 2 Mannschaften im Punktspielbetrieb aktiv. Der sportliche Erfolg ist gewährleistet durch
engagiertes und regelmäßiges Training, Integration aller Mitglieder und nicht zuletzt zahlreiche gesellige
Veranstaltungen.</p>

<p>Allen Interessenten und Neulingen empfehlen wir außerdem die Lektüre der Unterrubriken. Sie geben einen guten
Einblick in die Sparte Badminton. Hier findet ihr unsere Ziele und lernt wie die Sparte aufgebaut ist. In der
Bildergalerie sind Aufnahmen und Berichte aller bisher stattgefundenen Events zu finden.</p>
