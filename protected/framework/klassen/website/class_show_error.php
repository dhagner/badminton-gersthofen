<?php
include_once(dirname(__FILE__).'/class_show_exception.php');

/*******************************************************************************************************************//**
 * Exception, die im Falle eines (vorhergesehenen) Fehlers geworfen wird.
 **********************************************************************************************************************/
class CShowError extends CShowException
{
	/*****************************************************************************************************************//**
	 * @name Magics
	 **************************************************************************************************************//*@{*/

	public function __construct($Msg) {parent::__construct($Msg);}

	/*@}*/

	/*****************************************************************************************************************//**
	 * @name Verschiedenes
	 **************************************************************************************************************//*@{*/

	public function getTemplateData() {
		return new CTemplateData(parent::getDataArrayWithMsg());
	}

	/*@}*/
}
?>