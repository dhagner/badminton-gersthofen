<?php
/*******************************************************************************************************************//**
 * Basisklasse-Exception für auf der Website anzuzeigende Meldungen.
 **********************************************************************************************************************/
class CShowException extends Exception
{
	/*****************************************************************************************************************//**
	 * @name Magics
	 **************************************************************************************************************//*@{*/

	public function __construct($Msg) {parent::__construct($Msg);}

	/*@}*/

	/*****************************************************************************************************************//**
	 * @name Verschiedenes
	 **************************************************************************************************************//*@{*/

	public function getDataArrayWithMsg()
	{
		$data['msg'] = parent::getMessage();
		return $data;
	}

	/*@}*/
}
?>